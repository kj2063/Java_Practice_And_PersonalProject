package CH6_확인문제;

public class Printer {
	public static void println(int a) {
		System.out.println(a);
	}
	public static void println(boolean a) {
		System.out.println(a);
	}
	public static void println(double a) {
		System.out.println(a);
	}
	public static void println(String a) {
		System.out.println(a);
	}
}
